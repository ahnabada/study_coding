package study.ahnabada.programmers.code.brute_force.소수찾기;

import java.util.*;

public class Solution_jh {

    public int solution(String numbers) {
        int answer = 0;
        int[] num_arr = new int[numbers.length()];
        for (int i = 0; i < num_arr.length; i++) {
            num_arr[i] = numbers.charAt(i);
        }


        return answer;
    }
}
